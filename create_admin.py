def check_env_file():
    """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç –Ω–∞–ª–∏—á–∏–µ –∏ —Å–æ–¥–µ—Ä–∂–∏–º–æ–µ .env —Ñ–∞–π–ª–∞"""
    print("üîç –ü—Ä–æ–≤–µ—Ä–∫–∞ .env —Ñ–∞–π–ª–∞...")
    
    env_file = BASE_DIR / '.env'
    if env_file.exists():
        print(f"‚úÖ –§–∞–π–ª .env –Ω–∞–π–¥–µ–Ω: {env_file}")
        
        with open(env_file, 'r', encoding='utf-8') as f:
            env_lines = [line.strip() for line in f.readlines() if line.strip() and not line.startswith('#')]
            
        print(f"üìä –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö: {len(env_lines)}")
        
        # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –∫–ª—é—á–∏ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö
        print("üîê –ü–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –æ–∫—Ä—É–∂–µ–Ω–∏—è:")
        for line in env_lines[:10]:  # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –ø–µ—Ä–≤—ã–µ 10
            if '=' in line:
                key = line.split('=')[0]
                print(f"   ‚Ä¢ {key}")
        
        if len(env_lines) > 10:
            print(f"   ... –∏ –µ—â–µ {len(env_lines) - 10} –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö")
            
        return True
    else:
        print(f"‚ùå –§–∞–π–ª .env –Ω–µ –Ω–∞–π–¥–µ–Ω: {env_file}")
        return False

